namespace LankaConnect.Domain.Common.Enums;

/// <summary>
/// Diaspora engagement type classification for Sri Lankan communities worldwide
/// Defines different levels and types of engagement with cultural platform
/// </summary>
public enum DiasporaEngagementType
{
    /// <summary>
    /// Passive observer - consumes content without active participation
    /// </summary>
    Observer = 1,

    /// <summary>
    /// Active participant - regularly engages with community content
    /// </summary>
    Participant = 2,

    /// <summary>
    /// Content contributor - creates and shares cultural content
    /// </summary>
    Contributor = 3,

    /// <summary>
    /// Community leader - moderates and guides community discussions
    /// </summary>
    Leader = 4,

    /// <summary>
    /// Cultural ambassador - represents Sri Lankan culture in their region
    /// </summary>
    Ambassador = 5,

    /// <summary>
    /// Event organizer - coordinates cultural events and gatherings
    /// </summary>
    EventOrganizer = 6,

    /// <summary>
    /// Business network member - focuses on professional connections
    /// </summary>
    BusinessNetwork = 7,

    /// <summary>
    /// Educational facilitator - provides learning resources and guidance
    /// </summary>
    Educator = 8,

    /// <summary>
    /// Cultural heritage preservationist - focuses on tradition preservation
    /// </summary>
    HeritagePreservationist = 9,

    /// <summary>
    /// Youth mentor - guides younger generation in cultural identity
    /// </summary>
    YouthMentor = 10,

    /// <summary>
    /// Religious community leader - leads spiritual and religious activities
    /// </summary>
    ReligiousLeader = 11,

    /// <summary>
    /// Advocacy champion - promotes Sri Lankan causes and awareness
    /// </summary>
    AdvocacyChampion = 12
}